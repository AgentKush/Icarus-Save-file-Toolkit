def get_swap_targets(current_type):
    """Get valid species swap targets (same category only).

    Returns list of type names, excluding the current type.
    """
    cat = get_category(current_type)
    if cat == "unknown":
        return []
    return sorted(
        t for t, c in _CATEGORIES.items()
        if c == cat and t != current_type
    )


def remap_talents(old_talents, old_type, new_type):
    """Remap talent points from old species to new species.

    Matches by display name. Returns (new_talents, lost_points):
    - new_talents: dict {talent_row_name: rank} for the new species
    - lost_points: int, total points that couldn't be mapped (refunded)
    """
    old_tree = get_talent_tree(old_type)
    new_tree = get_talent_tree(new_type)

    # Build display_name → talent mapping for both trees
    old_by_display = {t["display"]: t for t in old_tree}
    new_by_display = {t["display"]: t for t in new_tree}

    new_talents = {}
    lost_points = 0

    for old_row, rank in old_talents.items():
        if rank <= 0:
            continue

        # Find the display name for this old talent
        old_talent = old_by_display.get(
            next((t["display"] for t in old_tree if t["name"] == old_row), None)
        )
        if not old_talent:
            # Unknown old talent — refund all points
            lost_points += rank
            continue

        display = old_talent["display"]

        # Find matching talent in new tree by display name
        new_talent = new_by_display.get(display)
        if not new_talent:
            # No equivalent in new tree — refund
            lost_points += rank
            continue

        # Clamp rank to new talent's max_rank
        new_rank = min(rank, new_talent["max_rank"])
        lost_points += rank - new_rank  # refund overflow
        if new_rank > 0:
            new_talents[new_talent["name"]] = new_rank

    return new_talents, lost_points

